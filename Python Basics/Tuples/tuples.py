# What is a Tuple? 
# Tuples are similar to list when it comes to ordered sequence of elements 
# but the main difference between tuples and list is that TUPLES are IMMUTABLE !!
# Meaning -> TUples are used where we store data that is supposed to remain unchanged, like coordiantes or database records.

# SO Basically TUples are READ ONLY Data type.abs
# We can say tuples are used where data integrity is of utmost importance
# Ex: You got placed as a developer in your own college and you small brother got admitted in same college.
# If the data were in lists it could have been edited, you could have changed the grades but as for data integrity they are in tuples